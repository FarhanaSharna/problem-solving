import java.util.Scanner;
//https://www.codechef.com/problems/CREDSCORE
public class CREDSCORE {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        if(a>=750){
            System.out.println("YES");
        }else{
            System.out.println("NO");
        }
    }
}
