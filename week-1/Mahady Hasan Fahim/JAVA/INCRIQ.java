import java.util.Scanner;
//https://www.codechef.com/problems/INCRIQ
public class INCRIQ {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        if((a+7)>170){
            System.out.println("YES");
        }else{
            System.out.println("NO");
        }
    }
}
